
    drop table WS_CATEGORY if exists;

    drop table WS_ORDER if exists;

    drop table WS_PRICE_LIST if exists;

    drop table WS_STATE if exists;

    drop table WS_USER if exists;

    create table WS_CATEGORY (
        ID bigint generated by default as identity unique,
        CAT_NAME varchar(255) not null,
        primary key (ID)
    );

    create table WS_ORDER (
        ID bigint generated by default as identity unique,
        ORD_NUMBER varchar(255) not null,
        primary key (ID)
    );

    create table WS_PRICE_LIST (
        ID bigint generated by default as identity unique,
        PL_NUMBER varchar(255) not null,
        primary key (ID)
    );

    create table WS_STATE (
        ID bigint generated by default as identity unique,
        STA_NAME varchar(255) not null,
        primary key (ID)
    );

    create table WS_USER (
        ID bigint generated by default as identity unique,
        password varchar(255) not null unique,
        username varchar(255) not null unique,
        primary key (ID)
    );
