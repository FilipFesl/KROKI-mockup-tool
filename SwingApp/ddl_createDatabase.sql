    drop table PRO_USER if exists;

    create table PRO_PANEL (
        ID bigint generated by default as identity unique,
        PAN_COLOR varchar(255) not null,
        PAN_NAME varchar(255) not null,
        primary key (ID)
    );

    create table PRO_USER (
        ID bigint generated by default as identity unique,
        password varchar(255) not null unique,
        username varchar(255) not null unique,
        primary key (ID)
    );
